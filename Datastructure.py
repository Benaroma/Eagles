# list datastructure
my_list = ["Kenya", "Nigeria", "Tanzania", "Uganda", "Brazil", "Sweden"]
my_num = [2, 8, 1, 34, 9, 0, -1, 23]
my_num.sort()
print(my_num)
my_list.sort()
print(my_list)
my_list[2] = "South Africa"
print(my_list[2])
print(f"I am from {my_list[0]}")

# tuple datastructure
my_tuple = ("Banana", "Apples", "Mangoes", "Oranges")
print(my_tuple)

# set datastructure
my_set={"Toyota","Nissan","Mercedes","Mazda","Dodge"}
my_set.add("RollsRoyce")
print(my_set)

# dictionary datastructure
my_dic={"name":"Benson","age":19,"gender":"male"}
print(my_dic)
print(f"my age is {my_dic['age']}")