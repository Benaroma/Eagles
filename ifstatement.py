nambari = int(input("Enter number:"))

if nambari % 2 == 0:
    print(f"{nambari} is even number")
else:
    print(f"{nambari} is odd number")

age = int(input("Enter number"))

if age <= 100:
    print(f"{age} can vote")
elif age <= 100:
    print(f"{age} cannot vote")
else:
    print(f"{age} cannot vote")
